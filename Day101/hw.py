#ეს კოდი არის ძალიან მარტივი ვებგვერდი, რომელიც მუშაობს, როგორც “To-Do” აპლიკაცია (დავალებების სია). მომხმარებელი ტექსტურ ველში წერს დავალებას და ამატებს სიაში ღილაკის “Add Task” დაჭერით. არსებობს კიდევ ერთი ღილაკი, “Clear All Tasks”, რომელიც შლის ყველა დავალებას სიიდან.

#როგორ გამოიყურება:

#	1.	სათაური: “Simple to Do”.
	#2.	ტექსტური ველი: მომხმარებელი აქ შეჰყავს ახალი დავალება.
	#3.	ღილაკი “Add Task”: ამატებს დავალებას სიაში.
#	4.	დავალებების სია: ჩნდება ახალი დავალებები.
	#5.	ღილაკი “Clear All Tasks”: შლის ყველა დავალებას სიიდან.

#CSS ნაწილი გამოიყენება, რომ გვერდი ლამაზად გამოიყურებოდეს (მაგალითად, დავალებები იქნება ყუთებში ჩასმული და განლაგებული თანაბრად).
